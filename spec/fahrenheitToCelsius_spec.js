import { fahrenheitToCelsius } from "../fahrenheitToCelsius.js";
// Freezing point (32°F)
console.log(fahrenheitToCelsius(32));  // Should be 0°C

// Boiling point (212°F)
console.log(fahrenheitToCelsius(212));  // Should be 100°C

// Room temperature (70°F)
console.log(fahrenheitToCelsius(70));  // Should be approximately 21.1°C

